myArray = [0, False, True, 1]

i = 0
while(i < len(myArray)):
    if(myArray[i] is True ):
        print("It's a true")

    elif(myArray[i] is False):
        print("It's a False")

    elif(myArray[i] is 1):
        print("It's a 1")

    elif(myArray[i] is 0):
        print("It's a 0")

    i = i+1
